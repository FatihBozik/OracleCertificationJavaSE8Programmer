package operators_and_statements;

public class SwitchSample2 {

	public static void main(String[] args) {
		int a = 5;

		switch (a) {
		case 3:
			System.out.println("3");
		case 5:
			System.out.println("5");
		case 6:
			System.out.println("6");
			break;
		case 9:
			System.out.println("3");
		}

	}

}
